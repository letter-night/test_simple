# Main generator (aligned with cancer_simple.py outputs)
# --------------------------------------------------------------------------------------

def generate_pretraining_dataset(
		num_samples: int,
		seq_length: int,
		*,
		seed: Optional[int] = None,
		treatment_assigner: Optional[TreatmentAssigner] = None,
		outcome_generator: Optional[OutcomeGenerator] = None,
		initial_volume_sampler: Optional[Callable[[int], np.ndarray]] = None,
		patient_type_sampler: Optional[Callable[[int], np.ndarray]] = None,
) -> Dict[str, np.ndarray]:
	"""
	Generate ONE synthetic dataset (one task) with num_samples trajectories.
	
	- A single treatment network and outcome network is sampled once and reused.
	- At each t:
		A_t sampled from treatment net given current covariate X_t
		Y_t sampled from outcome net given X_t, A_t, S
	
	Returns dict compatible with cancer_simple.py consumers.
	"""

	if seed is not None:
		np.random.seed(seed)
	
	# Sample patient-level static features and initial volumes
	if initial_volume_sampler is None:
		initial_volumes = _sample_initial_volumes(num_samples)
	else:
		initial_volumes = initial_volume_sampler(num_samples)
	
	if patient_type_sampler is None:
		patient_types = np.random.choice([1, 2, 3], size=num_samples)
	else:
		patient_types = patient_type_sampler(num_samples)
	
	# Static feature tensor S (n, ds). Keep ds=1 for alignment (patient_types as scalar).
	# S = patient_types.astype(np.float32).reshape(-1, 1)
	S = patient_types.reshape(-1, 1)

	# Create / sample mechanisms (fixed within this dataset)
	if treatment_assigner is None:
		treatment_assigner = TreatmentAssigner()
	if outcome_generator is None:
		outcome_generator = OutcomeGenerator()
	
	# Covariate is previous cancer volume => dx=1
	treatment_assigner.sample_new_mechanism(input_dim=1)
	outcome_generator.sample_new_mechanism(input_dim=1 + 1 + S.shape[1])

	# Allocate outputs (same style as cancer_simple)
	cancer_volume = np.zeros((num_samples, seq_length))#, dtype=np.float32)
	radio_application = np.zeros((num_samples, seq_length))#, dtype=np.int8)

	sequence_lengths = np.full((num_samples,), seq_length)#, dtype=np.int32)

	# Set initial covariate at t=0
	cancer_volume[:, 0] = initial_volumes#.astype(np.float32)

	for i in range(num_samples):
		for t in range(seq_length):
			X_t = cancer_volume[i, t].reshape(-1, 1)

			# Treatment at time t
			A_t = treatment_assigner.generate_treatments(X_t, resample=False)

			# radio_application[i, t] = np.int8(A_t[0])
			radio_application[i, t] = A_t[0]

			# current outcome becomes next covariate
			if t < seq_length - 1:
				S_i = S[i:i+1]
				y_next, _, _ = outcome_generator.generate_outcomes(X_t, A_t, S_i, resample=False)
				# cancer_volume[i, t+1] = np.float32(y_next[0])
				cancer_volume[i, t+1] = y_next[0]

	outputs = {
		"cancer_volume": cancer_volume,
		"radio_application": radio_application,
		"sequence_lengths": sequence_lengths,
		"patient_types": patient_types,
	}

	return outputs


def get_pretrain_scaling_params(sim: Dict[str, np.ndarray]) -> Tuple[pd.Series, pd.Series]:
	
	means = {}
	stds = {}
	
	means['cancer_volume'] = np.mean(sim['cancer_volume'])
	stds['cancer_volume'] = np.std(sim['cancer_volume'])

	# Add means for static variables
	means['patient_types'] = np.mean(sim['patient_types'])
	stds['patient_types'] = np.std(sim['patient_types'])

	return pd.Series(means), pd.Series(stds)


# Generate N datasets (tasks)
def generate_many_pretraining_datasets(
		num_datasets: int,
		samples_per_dataset: int,
		seq_length: int,
		*, 
		base_seed: Optional[int] = None,
		**kwargs,
) -> list[Dict[str, np.ndarray]]:
	"""
	Returns a list of datasets [D_0, ..., D_{N-1}], each generated with a different mechanism.
	Useful if PFN training samples "a dataset/task" per batch element.
	"""
	out = []
	for i in range(num_datasets):
		seed_i = None if base_seed is None else base_seed + i
		ds = generate_pretraining_dataset(
			samples_per_dataset, 
			seq_length, 
			seed=seed_i, 
			**kwargs,
		)
		out.append(ds)
	return out 



