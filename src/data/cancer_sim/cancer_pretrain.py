"""
Pre-training dataset generation for DynamicCausalPFN.

After pre-training on these datasets, 
we will evaluate DynamicCausalPFN's performance on cancer test dataset - zeroshot, fewshot, fine-tuning

Therefore, a large-scale of pre-training dataset is generated
baesd on DAG structure following cancer simulation data
(Time-varying covariate (prev_outcome (prev tumour volume)),
static_feature (patient types),
treatment (radiotherapy),
outcome (tumour volume)).

sequence_length = 30
projection_horizon = 2 (tau = 2 + 1 = 3)

At each timestep,
1) treatment is generated by treatment network given current covariate.
2) outcome is generated by outcome network given coviarate, treatment, static feature.

current outcome is then put into the next timestep's covariate.

And again generate treatment, outcome, .. and so on. 

Iterate this procedure to make timeseries of length = 30.

Given network parameters, noises, etc, a dataset is filled with M timeseries samples.

Do this for different parameters, and a large-scale dataset is generated. (N datasets).


We generate synthetic datasets (tasks) as follows. 
Each dataset is produced by:
- Sample ONE treatment network and ONE outcome network (fixed within the dataset).
- Generating M trajectories (samples), each of length T=seq_length.

DAG at each time step t:
	X_t -> A_t
	X_t -> Y_t
	A_t -> Y_t
	S    -> Y_t

	Dynamics:
		X_0 sampled from a realistic tumor-volume distribution
		For t=0...T-2:
			A_t ~ Bernoulli( sigmoid( f_A(X_t) ) )
			Y_t(a) = f_Y([X_t, a, S]) for a in {0, 1}
			Y_t = Y_t(A_t)
			X_{t+1} <- Y_t
	
	Outputs are aligned with cancer_simple.py keys:
		cancer_volume, radio_application, sequence_lengths, patient_types
	"""

from typing import Callable, Dict, Optional, Tuple

import numpy as np
import pandas as pd

MAX_VALUE = 100.0 

def _sample_initial_volumes(num_patients: int) -> np.ndarray:
	"""
	Sample initial tumour volumes 
	"""
	x0 = np.random.lognormal(mean=0.0, sigma=1.0, size=num_patients)
	x0 = np.clip(x0, 1e-3, None)#.astype(np.float64)
	
	return x0 


def _softplus(x: np.ndarray) -> np.ndarray:
	# numerically stable softplus
	return np.log1p(np.exp(-np.abs(x))) + np.maximum(x, 0.0)


def _sigmoid(x: np.ndarray) -> np.ndarray:
	return 1.0 / (1.0 + np.exp(-np.clip(x, -50, 50)))


