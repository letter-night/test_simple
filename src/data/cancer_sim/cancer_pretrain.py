class TreatmentAssigner:
	"""
	Stochastic propensity network:
		logit = f_A(X) with additive noise per layer,
		A ~ Bernoulli(sigmoid(logit))
	The sampled network parameters are FIXED for the whole dataset (task),
	but exogenous noise is sampled per call (per sample/time).
	"""

	def __init__(
			self, 
			prior_layers: Callable[[], int] = lambda: np.random.randint(2, 4), # (3, 5)
			prior_hidden_size: Callable[[], int] = lambda: np.random.randint(8, 30), # (8, 20)
			weight_std:float=1.0, # 0.8
			activation: Callable[[np.ndarray], np.ndarray] = np.tanh,
	):
		self.prior_layers=prior_layers
		self.prior_hidden_size=prior_hidden_size
		self.weight_std=weight_std
		self.activation=activation

		self.layer_weights: list[np.ndarray] = []
		self.layer_biases: list[np.ndarray] = []
		self.noise_samplers: list[Callable[[Tuple[int, int]], np.ndarray]] = []

		self.initialized: bool=False
		self.propensity_scores: Optional[np.ndarray] = None 
	
	def _sample_noise_distribution(self) -> Callable[[Tuple[int, int]], np.ndarray]:
		dist_type = np.random.choice(["normal", "uniform", "laplace", "logistic"])
		if dist_type == "normal":
			scale = np.random.uniform(0.05, 0.5) # (0.1, 1.0)
			return lambda shape: np.random.normal(0.0, scale, size=shape)
		if dist_type == "uniform":
			scale = np.random.uniform(0.05, 0.5) # (0.1, 1.0)
			return lambda shape: np.random.uniform(-scale, scale, size=shape)
		if dist_type == "laplace":
			scale = np.random.uniform(0.05, 0.3) # (0.1, 0.5)
			return lambda shape: np.random.laplace(0.0, scale, size=shape)
		# logistic
		scale = np.random.uniform(0.05, 0.3) # (0.1, 0.5)
		return lambda shape: np.random.logistic(0.0, scale, size=shape)
	
	def sample_new_mechanism(self, input_dim:int) -> None:
		depth = int(np.clip(self.prior_layers(), 2, 3)) # 3, 5
		hidden = int(self.prior_hidden_size())

		dims = [input_dim] + ([hidden] * (depth - 2)) + [1]

		self.layer_weights = []
		self.layer_biases = []
		self.noise_samplers = []

		for din, dout in zip(dims[:-1], dims[1:]):
			W = np.random.normal(0.0, self.weight_std / np.sqrt(max(din, 1)), size=(din, dout)) # normal(0.0, self.weight_std)
			b = np.random.normal(0.0, self.weight_std, size=(dout,))
			self.layer_weights.append(W)
			self.layer_biases.append(b)
			self.noise_samplers.append(self._sample_noise_distribution())
		
		self.initialized = True
	
	def _forward_logits(self, X: np.ndarray) -> np.ndarray:
		n = X.shape[0]
		h = X 
		for li, (W, b, noise_sampler) in enumerate(zip(self.layer_weights, self.layer_biases, self.noise_samplers)):
			eps = noise_sampler((n, W.shape[1]))
			h = h @ W + b + eps
			if li < len(self.layer_weights) - 1:
				h = self.activation(h)
		return h.reshape(-1)
	
	def generate_treatments(self, X: np.ndarray, resample: bool=False) -> np.ndarray:
		if (not self.initialized) or resample:
			self.sample_new_mechanism(input_dim=X.shape[1])
		
		logits = self._forward_logits(X)
		p = _sigmoid(logits)
		self.propensity_scores = p
		A = np.random.binomial(1, p)#.astype(np.int64)
		return A 
	

class OutcomeGenerator:
	"""
	Stochastic outcome network with edge-drop on hidden weights:
		Y(a) = f_Y([X, a, S]) with additive noise per layer
	Potential outcomes Y(0), Y(1) share the SAME sampled exogenous noise per call
	(per sample/time).
	"""
	def __init__(
			self, 
			prior_layers: Callable[[], int] = lambda: np.random.randint(2, 4), # (3, 6)
			prior_hidden_size: Callable[[], int] = lambda: np.random.randint(8, 30), # (10, 25)
			weight_std: float=1.0,
			edge_drop_prob: float=0.3,
			activation: Callable[[np.ndarray], np.ndarray] = np.tanh,
	):
		self.prior_layers=prior_layers
		self.prior_hidden_size=prior_hidden_size
		self.weight_std=weight_std
		self.edge_drop_prob=edge_drop_prob
		self.activation=activation


		self.layer_weights: list[np.ndarray] = []
		self.layer_biases: list[np.ndarray] = []
		self.noise_samplers: list[Callable[[Tuple[int, int]], np.ndarray]] = []

		self.initialized: bool=False

	
	def _sample_noise_distribution(self) -> Callable[[Tuple[int, int]], np.ndarray]:
		dist_type = np.random.choice(["normal", "uniform", "laplace", "logistic"])
		if dist_type == "normal":
			scale = np.random.uniform(0.05, 0.5) # (0.1, 1.0)
			return lambda shape: np.random.normal(0.0, scale, size=shape)
		if dist_type == "uniform":
			scale = np.random.uniform(0.05, 0.5) # (0.1, 1.0)
			return lambda shape: np.random.uniform(-scale, scale, size=shape)
		if dist_type == "laplace":
			scale = np.random.uniform(0.05, 0.3) # (0.1, 0.5)
			return lambda shape: np.random.laplace(0.0, scale, size=shape)
		scale = np.random.uniform(0.05, 0.3) # (0.1, 0.5)
		return lambda shape: np.random.logistic(0.0, scale, size=shape)
	
	def sample_new_mechanism(self, input_dim:int) -> None:
		depth = int(np.clip(self.prior_layers(), 2, 3)) # 3, 6
		hidden = int(self.prior_hidden_size())

		dims = [input_dim] + ([hidden] * (depth - 2)) + [1]

		self.layer_weights = []
		self.layer_biases = []
		self.noise_samplers = []

		for li, (din, dout) in enumerate(zip(dims[:-1], dims[1:])):
			W = np.random.normal(0.0, self.weight_std / np.sqrt(max(din, 1)), size=(din, dout)) # normal(0.0, self.weight_std)

			# Drop edges for hidden layers only (not outout layer)
			if self.edge_drop_prob > 0.0 and li < len(dims) - 2:
				mask = (np.random.rand(*W.shape) > self.edge_drop_prob).astype(W.dtype)
				W = W * mask 
			
			b = np.random.normal(0.0, self.weight_std, size=(dout,))
			self.layer_weights.append(W)
			self.layer_biases.append(b)
			self.noise_samplers.append(self._sample_noise_distribution())
		
		self.initialized = True
	
	def _forward_raw(self, Z: np.ndarray, noise_list: Optional[list[np.ndarray]] = None) -> Tuple[np.ndarray, list[np.ndarray]]:
		n = Z.shape[0]
		h = Z
		used_noise: list[np.ndarray] = []

		for li, (W, b, noise_sampler) in enumerate(zip(self.layer_weights, self.layer_biases, self.noise_samplers)):
			eps = noise_sampler((n, W.shape[1])) if noise_list is None else noise_list[li]
			used_noise.append(eps)

			h = h @ W + b + eps
			if li < len(self.layer_weights) - 1:
				h = self.activation(h)
		
		return h.reshape(-1), used_noise
	
	def _to_volume(self, raw: np.ndarray) -> np.ndarray:
		vol = _softplus(raw) 
		vol = np.clip(vol, 0.0, MAX_VALUE)
		return vol 
	
	def generate_outcomes(
			self, 
			X: np.ndarray,
			A: np.ndarray,
			S: np.ndarray,
			resample: bool = False,
	) -> Tuple[np.ndarray, np.ndarray, np.ndarray]:
		"""
		Args:
			X: (n, dx)
			A: (n,) int {0, 1}
			S: (n, ds)
		Returns:
			Y: (n,) factual
			Y0: (n,) potential under 0
			Y1: (n,) potential under 1
		"""
		if (not self.initialized) or resample:
			input_dim = X.shape[1] + 1 + S.shape[1]
			self.sample_new_mechanism(input_dim=input_dim)
		
		n = X.shape[0]
		a0 = np.zeros((n, 1), dtype=X.dtype)
		a1 = np.ones((n, 1), dtype=X.dtype)

		z0 = np.concatenate([X, a0, S], axis=1)
		z1 = np.concatenate([X, a1, S], axis=1)

		# Shared exogenous noise between Y(0) and Y(1) per sample/time
		_, noise = self._forward_raw(z0, noise_list=None)
		raw0, _ = self._forward_raw(z0, noise_list=noise)
		raw1, _ = self._forward_raw(z1, noise_list=noise)

		y0 = self._to_volume(raw0)
		y1 = self._to_volume(raw1)
		y = np.where(A.astype(bool), y1, y0)

		return y, y0, y1 
	

